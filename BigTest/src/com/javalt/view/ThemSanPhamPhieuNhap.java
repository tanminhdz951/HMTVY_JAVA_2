    public ThemSanPhamPhieuNhap(String maPN) {
        initComponents();
        this.maPN = maPN;
        proService = new UserDao_SanPham();
        setComboBoxItem(proService.getListsanpham());
        billInfoService = new UserDao_CTPhieuNhap();
        dfTableModel = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        productTable.setModel(dfTableModel);
        dfTableModel.addColumn("Mã sản phẩm");
        dfTableModel.addColumn("Số lượng");
        dfTableModel.addColumn("Giá tiền");
        if (billInfoService.getUserByMaPN(maPN) != null) {
            setTableData(billInfoService.getUserByMaPN(maPN));
        }
        listSelectionModel = productTable.getSelectionModel();
        listSelectionModel.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        listSelectionModel.addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                int row = productTable.getSelectedRow();
                String maSP = String.valueOf(productTable.getValueAt(row, 0));
                productComboBox.setSelectedItem(convertMaSPToTenSP(proService.getListsanpham(), maSP));
                int soLg = (int) productTable.getValueAt(row, 1);
                quantitySpinner.setValue(soLg);
                priceTextField.setText(String.valueOf(productTable.getValueAt(row, 2)));
            }
        });
        billInfo = new chiTietPhieuNhap();
        billInfo.setMaPN(maPN);
//        billInfo.setMaDH(maDH);
//        System.out.println(maPX);
//        jLabel6.setText(maPX);
        thanhTien = 0L;
    
    }
