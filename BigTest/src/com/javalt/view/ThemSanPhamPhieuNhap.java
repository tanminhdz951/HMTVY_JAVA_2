    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        int row = productTable.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(ThemSanPhamPhieuNhap.this, "Vui lòng chọn sản phẩm!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } else {
            billInfo.setMaSP(convertTenSPToMaSP(proService.getListsanpham(), productComboBox.getSelectedItem().toString()));
            try {
                billInfoService.deleteSanPham(maPN, billInfo.getMaSP());
                JOptionPane.showMessageDialog(ThemSanPhamPhieuNhap.this, "Thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(ThemSanPhamPhieuNhap.this, "Thất bại", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            }
            thanhTien = thanhTien - Long.valueOf(priceTextField.getText());
            removeAllValue();
            billTextField.setText("" + thanhTien);
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

