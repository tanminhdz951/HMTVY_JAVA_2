    private void setComboBoxItem(List<User_SanPham> products) {
        for (User_SanPham p : products) {
            productComboBox.addItem(p.getTenSP());
        }
    }

    private void setTableData(List<chiTietPhieuXuat> billInfos) {
        for (chiTietPhieuXuat b : billInfos) {
            dfTableModel.addRow(new Object[]{b.getMaSP(), b.getSoLuong(), b.getGiaTien()});
        }
    }

    private String convertTenSPToMaSP(List<User_SanPham> pros, String tenSP) {
        String maSP = "";
        for (User_SanPham p : pros) {
            if (tenSP.equals(p.getTenSP())) {
                maSP = p.getMaSP();
            }
        }
        return maSP;
    }

    private String convertMaSPToTenSP(List<User_SanPham> pros, String maSP) {
        String tenSP = "";
        for (User_SanPham p : pros) {
            if (maSP.equals(p.getMaSP())) {
                tenSP = p.getTenSP();
            }
        }
        return tenSP;
    }

    private long getDonGiaFromTenSP(List<User_SanPham> pros, String tenSP) {
        long donGia = 0L;
        for (User_SanPham p : pros) {
            if (tenSP.equals(p.getTenSP())) {
                donGia = p.getDonGiaBan();
            }
        }
        return donGia;
    }

    private void removeAllValue() {
        quantitySpinner.setValue(1);
        priceTextField.setText("");
    }

