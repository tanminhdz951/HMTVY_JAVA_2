    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        int row = productTable.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(ThemSanPhamPhieuXuat.this, "Vui lòng chọn sản phẩm!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } else {
            billInfo.setMaSP(convertTenSPToMaSP(proService.getAllUsers(), productComboBox.getSelectedItem().toString()));
            try {
                billInfoService.deleteSanPham(maPX, billInfo.getMaSP());
                JOptionPane.showMessageDialog(ThemSanPhamPhieuXuat.this, "Thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(ThemSanPhamPhieuXuat.this, "Thất bại", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            }
            thanhTien = thanhTien - Long.valueOf(priceTextField.getText());
            removeAllValue();
            billTextField.setText("" + thanhTien);
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

