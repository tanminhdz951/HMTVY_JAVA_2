    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {

            Connection conn = ConnectSQLServer.getConnection();
            PreparedStatement com = conn.prepareStatement("Delete  tblPhieuNhap  where maPN=?");
            com.setString(1, tb.getValueAt(tb.getSelectedRow(), 0).toString());
            if (JOptionPane.showConfirmDialog(this, "Delete  tblPhieuNhap?", "confirm",
                    JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                com.executeUpdate();
                tbn.setRowCount(0);
                DisplayInTable();
                //throw new java.lang.UnsupportedOperationException("Not supported yet.");      
            }
        } catch (Exception e) {
            System.out.println(e.toString());
        }
        // TODO add your handling code xoaere:
    }//GEN-LAST:event_jButton3ActionPerformed
