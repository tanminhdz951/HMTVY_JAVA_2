    public void DisplayInTable() {
        try {
            conn = ConnectSQLServer.getConnection();
            st = conn.createStatement();
            rs = st.executeQuery("Select *from tblPhieuNhap");
            tb.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
